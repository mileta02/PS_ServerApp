    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        String url = jTextFieldURL.getText();
        String username = jTextFieldUser.getText();
        String pass = new String(jPasswordField.getPassword());
        String port = jTextFieldPort.getText();

        if (!valid(url, username, pass, port)) {
           // JOptionPane.showMessageDialog(this, "Greška prilikom unošenja podataka.", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!config.doesExist())
            config.createFile();
        config.setProperty("db_url", url);
        config.setProperty("db_user", username);
        config.setProperty("db_password", pass);
        config.setProperty("port", port);

        JOptionPane.showMessageDialog(this, "Konfiguracija sacuvana uspesno, restartujte server!", "Obaveštenje", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();

    }//GEN-LAST:event_jButtonSaveActionPerformed

