    private boolean valid(String url, String username, String pass, String port) {
        boolean isValid = true;
        
        jLabelPass.setText("");
        jLabelURL.setText("");
        jLabelUser.setText("");
        jLabelPort.setText("");
        
        if (url.isEmpty() || !url.matches("^jdbc:mysql://localhost:\\d{1,5}/[a-zA-Z_][a-zA-Z0-9_]*$")) {
            jLabelURL.setText("Neispravan URL!");
            isValid = false;
        }
        if (username.isEmpty()) {
            jLabelUser.setText("Polje korisnicko ime ne može biti prazno!");
            isValid = false;
        }
       // if (pass.isEmpty()) {
       //     jLabelPass.setText("Lozinka ne može biti prazna!");
       //     isValid = false;
       // }
       if(port.isEmpty()){
           jLabelPort.setText("Polje port ne moze biti prazno!");
       }
        if (!port.matches("^\\d+$") || Integer.parseInt(port) < 1 || Integer.parseInt(port) > 65535) {
            jLabelPort.setText("Port mora biti između 1 i 65535!");
            isValid = false;
        }
        return isValid;
    }

    private void fillFields() {
        jTextFieldPort.setText(config.getProperty("port"));
        jTextFieldURL.setText(config.getProperty("db_url"));
        jTextFieldUser.setText(config.getProperty("db_user"));
        jPasswordField.setText(config.getProperty("db_password"));
    }

